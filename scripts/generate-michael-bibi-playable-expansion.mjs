    sqlText(row.artist_bio),
    sqlText(row.artist_genre),
    sqlText(row.artist_profile_hint),
    sqlText(row.source),
    sqlText(row.source_url),
    sqlText(row.status || "published"),
    sqlJson(trackMetadata(row))
