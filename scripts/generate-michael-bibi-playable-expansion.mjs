async function collectDeezerRows() {
  const payload = await fetchJson(`https://api.deezer.com/artist/${DEEZER_ARTIST_ID}/top?limit=100`);
  const items = Array.isArray(payload?.data) ? payload.data : [];
  const rows = [];
  const seen = new Set();

  for (const item of items) {
    if (!exactArtistMatch(item?.artist?.name || "")) continue;
    if (!String(item?.preview || "").startsWith("http")) continue;
    const detail = await fetchJson(`https://api.deezer.com/track/${item.id}`) || {};
    const row = deezerTrackRow(item, detail);
    if (!row.song || !row.preview_url) continue;
    const key = normalize(row.song);
    if (!key || seen.has(key)) continue;
    seen.add(key);
    rows.push(row);
    await new Promise((resolve) => setTimeout(resolve, 60));
  }
  return rows;
}

